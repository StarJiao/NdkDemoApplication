public class BeanDemo {
    private String string;
    private int integer;
    private boolean bool;
    private float flo;
    private double dou;
    private char ch;
    private short sh;
    private byte by;

    public String getString() {
        return string;
    }

    public void setString(String string) {
        this.string = string;
    }

    public int getInteger() {
        return integer;
    }

    public void setInteger(int integer) {
        this.integer = integer;
    }

    public boolean isBool() {
        return bool;
    }

    public void setBool(boolean bool) {
        this.bool = bool;
    }

    public float getFloat() {
        return flo;
    }

    public void setFloat(float flo) {
        this.flo = flo;
    }

    public double getDouble() {
        return dou;
    }

    public void setDouble(double dou) {
        this.dou = dou;
    }

    public char getChar() {
        return ch;
    }

    public void setChar(char ch) {
        this.ch = ch;
    }

    public short getShort() {
        return sh;
    }

    public void setShort(short sh) {
        this.sh = sh;
    }

    public byte getByte() {
        return by;
    }

    public void setByte(byte by) {
        this.by = by;
    }
}
